#basic caluclator
num1=int(input("enter first number "))
num2=int(input("enter second number "))
op=(input("Enter your operator "))
if op=="+":
    print("the result is ",num1+num2)
if op=="-":
    print("the result is ", num1-num2)
if op=="*":
    print("the product if values is ", num1*num2)
if op=="/":
    print("the result is ",num1/num2)